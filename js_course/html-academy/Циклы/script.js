/* Техническое задание #1
Напишите программу, которая последовательно выводит в консоль числа в геометрической прогрессии.

Стартовое значение, с которого должна начаться последовательность, записано в переменную startNumber.

Множитель записан в переменную multiplier.

Количество чисел записано в переменную quantity.

Первый тест. Стартовое значение 3, множитель: 3, количество: 6. Ожидаю вывод в консоль: 3, 9, 27, 81, 243, 729.

Второй тест. Стартовое значение 5, множитель: 2, количество: 7. Ожидаю вывод в консоль: 5, 10, 20, 40, 80, 160, 320.

Третий тест. Стартовое значение 2, множитель: 7, количество: 5. Ожидаю вывод в консоль: 2, 14, 98, 686, 4802.


*/

function f1() {
    let startNumber = 1;
    let multiplier = 4;
    let quantity = 7;
    let out = document.querySelector('.out-1');


    for (let i = 0; i < quantity; i++) {
        out.innerHTML += startNumber + '<br>';
        startNumber *= multiplier;
    }
}

document.querySelector('.b-1').onclick = f1;



