/* Техническое задание #5

Напиши программу, которая определяет сколько цифр в одном числе.

Само число записано в переменную number.

Найди количество цифр в этом числе и запиши результат в переменную quantity.

Первый тест. Число 15200. Ожидаю количество цифр: 5

Второй тест. Число 5678. Ожидаю количество цифр: 4

Третий тест. Число 17. Ожидаю количество цифр: 2

Четвертый тест. Число 1. Ожидаю количество цифр: 1

*/



function f5() {
    let number = 123;
    let quantity = 0;

    while (number > 0) {
        number = Math.floor(number / 10);
        quantity++;
    };

    document.querySelector('.out-5').innerHTML = 'let quantity = ' + quantity + ';';
}

document.querySelector('.b-5').onclick = f5;



