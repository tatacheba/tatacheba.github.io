/* Техническое задание #7
Мяу! Напиши программу, которая проверяет, является ли число палиндромом.

Число записано в переменную poly.

Переменная, куда нужно записать «перевёрнутую» версию числа, называется ylop.

Выясни, является ли число из переменной poly палиндромом. Если да, значение флага isPalindrome должно быть true, если число не палиндром, то false.

Первый тест. Число: 7997. Ожидаю результат: число палиндром: true

Второй тест. Число: 5005. Ожидаю результат: число палиндром: true

Третий тест. Число: 1591. Ожидаю результат: число палиндром: false
*/



function f7() {
    let poly = 126621;
    let ylop = 0;
    let isPalindrome = false;

    let k = 0;
    ylop = poly.toString().split('');
    for (let i = 0; i < ylop.length / 2; i++) {
        if (ylop[i] == ylop[ylop.length - 1 - i]) {
            k++;
        }
    }
    if (k == ylop.length / 2) {
        isPalindrome = true;

    };

    document.querySelector('.out-7').innerHTML = 'let isPalindrome = ' + isPalindrome + ';';
}

document.querySelector('.b-7').onclick = f7;